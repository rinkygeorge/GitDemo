package Assignments;

public class Area {
	int a1, a2, a3, length, width, rectangle, side, square, amount;
	float n1, n2, n3;
	double average;
	float average2;
	double  radius, circle;
	
	public double math(int a1, int a2, int a3){
		average = (a1+a2+a3)/3;		
		return average;
		
	}
	public float math(float n1, float n2, float n3){
		average2 = (n1+n2+n3)/3;
		return average2;
		
	}
	public double areas(int length, int width){
		rectangle = length * width;
		return rectangle;
		
	}
	public double areas(double radius){
		circle = 3.14 * radius * radius;
		return circle;
		
	}
	public int areas(int side){
		square = side * side;
		return square;
		
	}
	public static void main(String[] args) {
		Area an = new Area();
		System.out.println(an.math(2,2,2));
		Area ar = new Area();
		System.out.println(ar.math(5.5f,2.5f,6.5f));
		Area rc = new Area();
		System.out.println("Area of rectangle "+ rc.areas(10,5)+"cm");
		Area cr = new Area();
		System.out.println("Area of circle "+cr.areas(6));
		Area sq = new Area();
		System.out.println("Area of square "+sq.areas(7));
		
		

	}

}
